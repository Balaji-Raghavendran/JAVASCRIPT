// program for post incrementing and post decrementing the given values

var a = 10
var b = a++
console.log(b)
console.log(a)

var a = 10
var b = a--
console.log(b)
console.log(a)


// program for pre incrementing and pre decrementing the given values

var a = 10
var b = ++a
console.log(b)
console.log(a)

var a = 20
var b = --a
console.log(b)
console.log(a)